# Function to return the midpoint between two numbers.
def midpoint(num1, num2):
    return (num1 + num2) / 2

# Function to return the square root of a number.
def square_root(x):
    return x ** 0.5

# Function to return the exponentiation of a number.
def exponent(base, exp):
    return base ** exp

# Function to take two numbers as input and returns
# Value of the larger one. 
def max(a, b):
    return a if a > b else b 

# Function to take two numbers as input and returns
# Value of the smaller one.
def min(a, b):
    return a if a < b else b
